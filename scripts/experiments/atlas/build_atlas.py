"""Build a browsable atlas from pilot classifications.

Groups pilot papers by the canonical structure (template, collapsed by the
equivalence graph) their core model instantiates, and emits:
  - results/atlas.json  — structured data
  - results/atlas.html  — a self-contained browsable page

The interesting rows are templates that collect papers from >= 2 distinct
arXiv archives: those are the cross-domain structural bridges the whole
project exists to surface.
"""

import html
import json
import os
from collections import defaultdict

HERE = os.path.dirname(os.path.abspath(__file__))
CLS_DIR = os.path.join(HERE, 'classifications_pilot')
BUNDLES = os.path.join(HERE, 'data', 'pilot_bundles')


def load_equiv():
    path = os.path.join(HERE, 'template_equivalences.json')
    rep, label = {}, {}
    if os.path.exists(path):
        for group in json.load(open(path))['groups']:
            key = group[0]
            for tid in group:
                rep[tid] = key
    return rep


def archive(cat):
    return cat.split('.')[0] if cat else 'unknown'


ARCHIVE_LABEL = {
    'math': 'mathematics', 'physics': 'physics', 'cond-mat': 'condensed matter',
    'cs': 'computer science', 'q-bio': 'biology', 'q-fin': 'finance',
    'econ': 'economics', 'eess': 'electrical eng', 'nlin': 'nonlinear',
    'astro-ph': 'astrophysics', 'stat': 'statistics',
}


