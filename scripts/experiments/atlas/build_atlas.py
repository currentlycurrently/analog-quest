def main():
    rep = load_equiv()
    templates = {t['template_id']: t for t in json.load(open(os.path.join(HERE, 'templates.json')))['templates']}

    # template_class -> list of {id, title, cat, archive, conf, twist}
    groups = defaultdict(list)
    n_papers, n_assigned = 0, 0
    for fn in sorted(os.listdir(CLS_DIR)) if os.path.isdir(CLS_DIR) else []:
        if not fn.endswith('.json'):
            continue
        c = json.load(open(os.path.join(CLS_DIR, fn)))
        n_papers += 1
        bpath = os.path.join(BUNDLES, fn)
        bundle = json.load(open(bpath)) if os.path.exists(bpath) else {}
        cat = bundle.get('primary_category', '')
        title = bundle.get('title', c['arxiv_id'])
        seen = set()
        for a in c.get('assignments', []):
            cls = rep.get(a['template_id'], a['template_id'])
            if cls in seen:
                continue
            seen.add(cls)
            n_assigned += 1
            groups[cls].append({
                'arxiv_id': c['arxiv_id'], 'title': title, 'cat': cat,
                'archive': archive(cat), 'conf': a.get('confidence', 0),
                'twist': a.get('twist', ''),
            })

    rows = []
    for cls, papers in groups.items():
        archives = sorted({p['archive'] for p in papers})
        rows.append({
            'template_class': cls,
            'name': templates.get(cls, {}).get('name', cls),
            'canonical_form': templates.get(cls, {}).get('canonical_form', ''),
            'n_papers': len(papers),
            'archives': archives,
            'n_archives': len(archives),
            'cross_domain': len(archives) >= 2,
            'papers': sorted(papers, key=lambda p: -p['conf']),
        })
    # cross-domain first, then by breadth, then by count
    rows.sort(key=lambda r: (-r['cross_domain'], -r['n_archives'], -r['n_papers']))

    out = {
        'n_papers': n_papers, 'n_assigned_slots': n_assigned,
        'n_structures': len(rows),
        'n_cross_domain': sum(1 for r in rows if r['cross_domain']),
        'structures': rows,
    }
    os.makedirs(os.path.join(HERE, 'results'), exist_ok=True)
    with open(os.path.join(HERE, 'results', 'atlas.json'), 'w') as f:
        json.dump(out, f, indent=1)

    write_html(out, rows)

    print('atlas: %d papers, %d structures, %d cross-domain' % (
        out['n_papers'], out['n_structures'], out['n_cross_domain']))
    print('cross-domain structures:')
    for r in rows:
        if r['cross_domain']:
            print('  %-32s %2d papers  [%s]' % (r['template_class'], r['n_papers'], ', '.join(r['archives'])))


if __name__ == '__main__':
    main()
